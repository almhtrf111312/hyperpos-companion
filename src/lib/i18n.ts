// Get current language from localStorage or default to Arabic
export function getCurrentLanguage(): Language {
  if (typeof window !== 'undefined') {
    const stored = localStorage.getItem('hyperpos_language');
    if (stored && languages.some(l => l.code === stored)) {
      return stored as Language;
    }
  }
  return 'ar';
}

// Set language
export function setLanguage(lang: Language): void {
  if (typeof window !== 'undefined') {
    localStorage.setItem('hyperpos_language', lang);
    // Update document direction
    const langInfo = languages.find(l => l.code === lang);
    if (langInfo) {
      document.documentElement.dir = langInfo.direction;
      document.documentElement.lang = lang;
    }
    // Dispatch custom event for language change
    window.dispatchEvent(new CustomEvent('languagechange', { detail: lang }));
  }
}

// Translate function
export function t(key: TranslationKey, lang?: Language): string {
  const currentLang = lang || getCurrentLanguage();
  const langTranslations = translations[currentLang];
  
  if (langTranslations && key in langTranslations) {
    return langTranslations[key as keyof typeof langTranslations];
  }
  
  // Fallback to Arabic, then English, then key
  if (translations.ar[key]) {
    return translations.ar[key];
  }
  if (translations.en[key]) {
    return translations.en[key];
  }
  
  return key;
}

// Initialize language direction on load
export function initializeLanguage(): void {
  const lang = getCurrentLanguage();
  const langInfo = languages.find(l => l.code === lang);
  if (langInfo && typeof window !== 'undefined') {
    document.documentElement.dir = langInfo.direction;
    document.documentElement.lang = lang;
  }
}
